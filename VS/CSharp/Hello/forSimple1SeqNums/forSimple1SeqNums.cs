using System;
/*
 * Напишете програма, която отпечатва числата от 1 до 100, по едно на ред.
 * https://judge.softuni.bg/Contests/Practice/Index/616#0
 */
namespace forSimple1SeqNums
{
    class forSimple1SeqNums
    {
        static void Main(string[] args)
        {
            for (int i = 0; i < 100; i++)
            {
                Console.WriteLine((i+1));
            }
        }
    }
}
