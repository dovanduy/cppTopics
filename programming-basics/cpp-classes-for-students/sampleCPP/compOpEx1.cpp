#include <iostream>

using	namespace std ;

main ()
{
	int a=5, b=11 ;
    cout    <<"\na=" <<a <<"\tb=" <<b <<'\n' ;
    cout    <<"\na-b>b-a is\t" <<(a-b>b-a) ;
    cout    <<"\na-b<b-a is\t" <<(a-b<b-a) <<'\n' ;
    cout    <<"\na!=b is\t\t" <<(a!=b) ;
    cout    <<"\na==b is\t\t" <<(a==b) <<'\n' ;
}

